    private void BtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtSalvarActionPerformed
        // BOTÃO SALVAR
        if (salvarAlterar.equals("novo")) {

            modelFornecedor.setNomeFornecedor(this.InputNome.getText());
            modelFornecedor.setCnpjFornecedor(this.InputCNPJ.getText());
            modelFornecedor.setTelefoneFornecedor(this.InputTelefone.getText());
            modelFornecedor.setEmailFornecedor(this.InputEmail.getText());

            if (controllerFornecedor.salvarFornecedorController(modelFornecedor) > 0) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso");
                //CARREGAR CLIENTES NA TABELA
                mostrarFornecedor();
                this.habilitaDesabilitaCampos(false);
                this.LimparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar!");
            }

        } else {
            //Mudar aqui
            modelFornecedor.setIdFornecedor(Integer.parseInt(this.InputCodigo.getText()));
            modelFornecedor.setNomeFornecedor(this.InputNome.getText());
            modelFornecedor.setCnpjFornecedor(this.InputCNPJ.getText());
            modelFornecedor.setTelefoneFornecedor(this.InputTelefone.getText());
            modelFornecedor.setEmailFornecedor(this.InputEmail.getText());

            if (controllerFornecedor.atualizarFornecedorController(modelFornecedor)) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso");
                //CARREGAR CLIENTES NA TABELA
                mostrarFornecedor();
                // InputCodigo.setText("");
                this.habilitaDesabilitaCampos(false);
                this.LimparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar!");
            }

        }

    }//GEN-LAST:event_BtSalvarActionPerformed

    private void BtDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtDeletarActionPerformed
        // TODO add your handling code here:
        //BOTÃO DELETAR
        try {
            int linha = TabelaFornecedor.getSelectedRow();
            int codigoFornecedor = (int) TabelaFornecedor.getValueAt(linha, 0);
            if (controllerFornecedor.excluirFornecedorController(codigoFornecedor)) {
                JOptionPane.showMessageDialog(this, "Deletado com sucesso");
                this.mostrarFornecedor();
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao deletar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "NÃ£o existem dados para serem deletados!");
        }

    }//GEN-LAST:event_BtDeletarActionPerformed

    private void BtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlterarActionPerformed
        // TODO add your handling code here:
        // BOTÃO ALTERAR
        try {
            int linha = TabelaFornecedor.getSelectedRow();
            int codigoFornecedor = (int) TabelaFornecedor.getValueAt(linha, 0);
            salvarAlterar = "alterar";
            modelFornecedor = controllerFornecedor.getFornecedorController(codigoFornecedor);
            InputCodigo.setText(String.valueOf(modelFornecedor.getIdFornecedor()));
            InputNome.setText(modelFornecedor.getNomeFornecedor());
            InputCNPJ.setText(modelFornecedor.getCnpjFornecedor());
            InputTelefone.setText(modelFornecedor.getTelefoneFornecedor());
            InputEmail.setText(modelFornecedor.getEmailFornecedor());
            this.habilitaDesabilitaCampos(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Selecione um item para alterar");
        }


    }//GEN-LAST:event_BtAlterarActionPerformed

