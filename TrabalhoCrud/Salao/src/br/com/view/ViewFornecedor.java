    private void mostrarFornecedor() {

        listaModelFornecedor = controllerFornecedor.getListaFornecedorController();
        DefaultTableModel modelo = (DefaultTableModel) TabelaFornecedor.getModel();
        modelo.setNumRows(0);
        int cont = listaModelFornecedor.size();
        for (int i = 0; i < cont; i++) {
      
            modelo.addRow(new Object[]{
                    
                listaModelFornecedor.get(i).getIdFornecedor(),
                listaModelFornecedor.get(i).getNomeFornecedor(),
                listaModelFornecedor.get(i).getCnpjFornecedor(),
                listaModelFornecedor.get(i).getTelefoneFornecedor(),
                listaModelFornecedor.get(i).getEmailFornecedor()
                

            });
        }
    }

    private void habilitaDesabilitaCampos(boolean condicao) {
        InputNome.setEnabled(condicao);
        InputCNPJ.setEnabled(condicao);
        InputTelefone.setEnabled(condicao);
        InputEmail.setEnabled(condicao);
        BtSalvar.setEnabled(condicao);
    }

    private void LimparCampos() {
        InputNome.setText("");
        InputCNPJ.setText("");
        InputTelefone.setText("");
        InputEmail.setText("");
        InputCodigo.setText("");
    }

