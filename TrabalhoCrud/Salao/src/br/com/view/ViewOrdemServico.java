    //preenche o seletor de clientes
    private void listarClientes() {
        listaModelClientes = controllerCliente.getListaClienteController();
        InputNomeCliente.removeAllItems();
        for (int i = 0; i < listaModelClientes.size(); i++) {
            InputNomeCliente.addItem(listaModelClientes.get(i).getNOME_CLIENTE());
        }

    }

    private void listarServicos() {
        listaModelServico = controllerServico.retornaListaServicoController();
        InputNomeServico.removeAllItems();
        for (int i = 0; i < listaModelServico.size(); i++) {
            InputNomeServico.addItem(listaModelServico.get(i).getNOME_SERVICO());
        }

    }

    private void somarValorTotalServicos() {
        double soma = 0;
        double valor;
        int contador = tabelaDados.getRowCount();
        for (int i = 0; i < contador; i++) {
            valor = (double) tabelaDados.getValueAt(i, 4);
            soma += valor;
        }
        InputVaalorTotal.setText(String.valueOf(soma));
        desconto();
    }

    private void desconto() {
        try {
            InputVaalorTotal.setText(String.valueOf(
                    Double.parseDouble(InputVaalorTotal.getText()) - Double.parseDouble(InputDesconto.getText())));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Preencha de desconto");
        }

    }

    private void carregarOS() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaOS.getModel();
        listaModelOSCliente = controllerOSCliente.getListaOSClienteController();
        int cont = listaModelOSCliente.size();
        modelo.setNumRows(0);
        for (int i = 0; i < cont; i++) {

            modelo.addRow(new Object[]{
                listaModelOSCliente.get(i).getModelOrdemServico().getOrdemId(),
                listaModelOSCliente.get(i).getModelCliente().getNOME_CLIENTE(),
              
                listaModelOSCliente.get(i).getModelOrdemServico().getOrdemData()

            });
        }

    }

    private void limparInput() {
        InputQuantidade.setText("");
        InputDesconto.setText("");
        InputVaalorTotal.setText("");
        DefaultTableModel modelo = (DefaultTableModel) tabelaDados.getModel();
        modelo.setNumRows(0);

    }


