    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarActionPerformed
        // TODO add your handling code here:
        //BOTÃO DELETAR
        int linha = TabelaOS.getSelectedRow();
        int codigo = (int) TabelaOS.getValueAt(linha, 0);
        try {
           
            controllerServicosPrestados.excluirServicosPrestadosController(codigo);
            if (controllerOrdemServico.excluirOrdemServicoController(codigo)) {
               
                JOptionPane.showMessageDialog(this, "Excluido com sucesso");
                this.carregarOS();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro");
        }
       
    }//GEN-LAST:event_btDeletarActionPerformed

    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:
        //BOTÃO ADICIONAR
        if (InputQuantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            modelServico = controllerServico.retornListaServicoController(Integer.parseInt(InputCodigoServico.getText()));
            DefaultTableModel modelo = (DefaultTableModel) tabelaDados.getModel();
            int cont = 0;
            double quantidade;
            quantidade = Double.parseDouble(InputQuantidade.getText());
            for (int i = 0; i < cont; i++) {
                modelo.setNumRows(0);

            }
            modelo.addRow(new Object[]{
                modelServico.getID_SERVICO(),
               modelServico.getNOME_SERVICO(),
                InputQuantidade.getText(),
                modelServico.getVALOR_SERVICO(),
                quantidade * modelServico.getVALOR_SERVICO()
            }
            );
            somarValorTotalServicos();
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

