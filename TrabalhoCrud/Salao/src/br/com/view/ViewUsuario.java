    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // BOTÃO SALVAR
        try {
            modelUsuario.setIdUsuario(Integer.parseInt(InputCodigo.getText()));
        } catch (NumberFormatException e) {

        }

        String pwd = InputSenha.getText();
        String senhaHex = null;
        try {
            // TODO add your handling code here:
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte messageDigest[] = md.digest(pwd.getBytes("UTF-8"));

            StringBuilder sB = new StringBuilder();
            for (byte b : messageDigest) {
                sB.append(String.format("%02x", 0xFF & b));
                senhaHex = sB.toString();
            }

        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelUsuario.setLoginUsuario(InputLogin.getText());
        modelUsuario.setNomeUsuario(InputNome.getText());
        modelUsuario.setSenhaUsuario(senhaHex);
        if (alterarSalvar.equals("salvar")) {
            if (controllerUsuario.salvarUsuarioController(modelUsuario) > 0) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
                mostrarUsuario();
                habilitarDesebilitar(false);
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar!");
            }

        } else {

            if (controllerUsuario.atualizarUsuarioController(modelUsuario)) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
                mostrarUsuario();
                habilitarDesebilitar(false);
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar!");
            }

        }


    }//GEN-LAST:event_btSalvarActionPerformed

    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarActionPerformed
        // BOTÃO USUARIO
        int linha = TabelaUsuario.getSelectedRow();
        int codigoUsuario = (int) TabelaUsuario.getValueAt(linha, 0);
        if (controllerUsuario.excluirUsuarioController(codigoUsuario)) {
            JOptionPane.showMessageDialog(this, "Deletado com sucesso");
            this.mostrarUsuario();
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao deletar");
        }
    }//GEN-LAST:event_btDeletarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // BOTÃO ALTERAR
        String pwd = InputSenha.getText();
        String senhaHex = null;
        try {
            // TODO add your handling code here:
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte messageDigest[] = md.digest(pwd.getBytes("UTF-8"));

            StringBuilder sB = new StringBuilder();
            for (byte b : messageDigest) {
                sB.append(String.format("%02x", 0xFF & b));
                senhaHex = sB.toString();
            }

        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        int linha = TabelaUsuario.getSelectedRow();
        int Codigo = (int) TabelaUsuario.getValueAt(linha, 0);
        modelUsuario = controllerUsuario.getUsuarioController(Codigo);
        InputCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
        InputNome.setText(modelUsuario.getNomeUsuario());
        InputLogin.setText(modelUsuario.getLoginUsuario());
        InputSenha.setText(senhaHex);
        alterarSalvar = "alterar";
        habilitarDesebilitar(true);
    }//GEN-LAST:event_btAlterarActionPerformed

