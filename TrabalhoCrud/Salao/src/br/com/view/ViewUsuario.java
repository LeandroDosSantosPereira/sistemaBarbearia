    private void mostrarUsuario() {
        listaModelUsuario = controllerUsuario.getListaUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) TabelaUsuario.getModel();
        modelo.setNumRows(0);
        int contadora = listaModelUsuario.size();
        for (int i = 0; i < contadora; i++) {
            modelo.addRow(new Object[]{
                listaModelUsuario.get(i).getIdUsuario(),
                listaModelUsuario.get(i).getNomeUsuario(),
                listaModelUsuario.get(i).getLoginUsuario()
            //listaModelUsuario.get(i).getSenhaUsuario()
            }
            );
        }
    }

