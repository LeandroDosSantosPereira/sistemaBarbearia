    private void BtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlterarActionPerformed
        // BOTÃO ALTERAR
        habilitaDesabilitaInputs(true);
        int linhaAlterar = this.TabelaServico.getSelectedRow();
        salvarAlteração="alterar";        
        try {
            // BUSCA NO BANCO
            int codigoServico = (int) this.TabelaServico.getValueAt(linhaAlterar,0);
            modelServico=controlerServico.retornListaServicoController(codigoServico);
            //JOGAR ITEN SELECIONADO NA INTERFACE
            this.InputCodigoServico.setText(String.valueOf(modelServico.getID_SERVICO()));
            this.InputNomeServico.setText(modelServico.getNOME_SERVICO());
            this.InputValorServico.setText(String.valueOf(modelServico.getVALOR_SERVICO()));
          
        } catch (Exception e) {
            habilitaDesabilitaInputs(false);
            JOptionPane.showMessageDialog(this,"Erro ao alterar!");
        }
    }//GEN-LAST:event_BtAlterarActionPerformed

    private void BtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtPesquisarActionPerformed
        // BOTÃO PESQUISAR
        DefaultTableModel modelo = (DefaultTableModel) this.TabelaServico.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.TabelaServico.setRowSorter(classificador);
        String texto = InputPesquisarServico.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
        
    }//GEN-LAST:event_BtPesquisarActionPerformed

