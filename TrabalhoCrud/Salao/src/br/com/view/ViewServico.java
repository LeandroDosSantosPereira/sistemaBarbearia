    //HABILITA OS CAMPOS
    private void habilitaDesabilitaInputs(boolean condicao)
    {
        
        InputNomeServico.setEnabled(condicao);
        InputValorServico.setEnabled(condicao);
        BtSalvar.setEnabled(condicao);
    }
    //LIMPA OS CAMPOS
    private void limparInputs()
    {
        InputNomeServico.setText("");
        InputValorServico.setText("");
    }
    
    private void salvarServico()
    {
        //METODO QUE SALVA OS SERVIÇOS
          modelServico.setNOME_SERVICO(this.InputNomeServico.getText());
          modelServico.setVALOR_SERVICO(formatador.converterVirgulaParaPonto(this.InputValorServico.getText()));
        if (controlerServico.cadastraServicoController(modelServico)>0)
        {
            JOptionPane.showMessageDialog(this, "Dados salvos com sucesso!");
             this.limparInputs();
             InputNomeServico.setEnabled(false);
             InputValorServico.setEnabled(false);
             habilitaDesabilitaInputs(false);
            mostrarServicos();
        }else{
            JOptionPane.showMessageDialog(this,"Erro ao cadastrar dados!");
        }
    }
    
    private void alterarServico()
    {
        //METODO QUE ALTERA OS SERVIÇOS
         modelServico.setNOME_SERVICO(this.InputNomeServico.getText());
         modelServico.setVALOR_SERVICO(formatador.converterVirgulaParaPonto(this.InputValorServico.getText()));
        if (controlerServico.alterarServicoController(modelServico))
        {
            JOptionPane.showMessageDialog(this, "Dados Alterados com sucesso!");
             this.limparInputs();
             InputNomeServico.setEnabled(false);
             InputValorServico.setEnabled(false);
             InputCodigoServico.setText("");
              habilitaDesabilitaInputs(false);
            mostrarServicos();
        }else{
            JOptionPane.showMessageDialog(this,"Erro ao alterar dados!");
        }
        
    }
    
    //JOGA OS SERVIÇOS NA TABELA

    private void mostrarServicos() {
        ListaModelServico = controlerServico.retornaListaServicoController();
        DefaultTableModel modelo = (DefaultTableModel) TabelaServico.getModel();
        modelo.setNumRows(0);
        int contador = ListaModelServico.size();
        for (int i = 0; i < contador; i++) {

            modelo.addRow(new Object[]{
                ListaModelServico.get(i).getID_SERVICO(),
                ListaModelServico.get(i).getNOME_SERVICO(),
                ListaModelServico.get(i).getVALOR_SERVICO()
            });
        }
    }

