    private void BtDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtDeletarActionPerformed
        // TODO add your handling code here:
        //BOTÃO DELETAR
        try {
            
             int linha = TabelaServico.getSelectedRow();
            int codigoServico = (int) TabelaServico.getValueAt(linha,0);
            if(controlerServico.deletarServicoController(codigoServico))
            {
                JOptionPane.showMessageDialog(this, "Dados deletados com sucesso!");
                this.habilitaDesabilitaInputs(false);
                InputNomeServico.setEnabled(false);
                InputValorServico.setEnabled(false);
                this.mostrarServicos();

            }else{
                JOptionPane.showMessageDialog(this, "Erro ao deletar dados!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Escolha um item para deletar");
        }
       
    }//GEN-LAST:event_BtDeletarActionPerformed

